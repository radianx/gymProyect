    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if(!jTable1.getSelectionModel().isSelectionEmpty()){
            movi = (Movimiento) jTable1.getValueAt(jTable1.getSelectedRow(),0);
            this.txtIdMovimiento.setText(String.valueOf(movi.getIdmovimiento()));
            this.txtTotal.setText(String.valueOf(movi.getMonto()));
            this.txtNombreusuario.setText(movi.getUsuario().getNombreusuario());
            Instant instante = movi.getHora().toInstant();
            LocalDateTime apertura = instante.atZone(ZoneId.systemDefault()).toLocalDateTime();
            this.timeHora.setDateTimePermissive(apertura);
            this.txtAbonado.setText(String.valueOf(movi.getMontoCliente()));
            this.txtEstado.setText(movi.getEstado());
            this.txtDetalle.setText(movi.getDetalle());
            this.txtIdCaja.setText(String.valueOf(movi.getCaja().getIdcaja()));
            this.txtVuelto.setText(String.valueOf(movi.getVuelto()));
        }
    }//GEN-LAST:event_jTable1MouseClicked

