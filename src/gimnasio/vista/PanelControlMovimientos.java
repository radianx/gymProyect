    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(movi!=null){
            try{
            Instant instante = timeHora.getDateTimePermissive().atZone(ZoneId.systemDefault()).toInstant();
            Date fecha = Date.from(instante);
            movi.setHora(fecha);
            movi.setDetalle(txtDetalle.getText());
            miControlador.getMiPersistencia().actualizarInstancia(movi);
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "El monto que desea guardar es invalido.");
            }catch(Notificaciones e){
                JOptionPane.showMessageDialog(null, "Ha ocurrido una excepcion con la base de datos");
                e.printStackTrace();
            }finally{
                limpiarCampos();
                movi= null;
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(movi!=null){
            try {
                movi.setEstado("CANCELADO");
                miControlador.getMiPersistencia().actualizarInstancia(movi);
                SwingUtilities.invokeLater(()->{
                    this.cargarTablaMovimientos();
                    this.limpiarCampos();
                });
            } catch (Notificaciones ex) {
                JOptionPane.showMessageDialog(null,"Se ha producido una excepcion.");
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


