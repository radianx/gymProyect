    public void cargarTablaMovimientos(){
        modeloTablaMovimientos = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        modeloTablaMovimientos.addColumn("IdMovimiento");
        modeloTablaMovimientos.addColumn("Usuario");
        modeloTablaMovimientos.addColumn("IdCaja");
        modeloTablaMovimientos.addColumn("Estado");
        modeloTablaMovimientos.addColumn("Total");
        modeloTablaMovimientos.addColumn("Abonado");
        modeloTablaMovimientos.addColumn("vuelto");
        modeloTablaMovimientos.addColumn("Detalle");
        modeloTablaMovimientos.addColumn("Fecha");
        Object[] fila = new Object[9];
        try{
            for(Movimiento mov:miControlador.getListaMovimientos()){
                fila[0] = mov;
                fila[1] = mov.getUsuario();
                fila[2] = mov.getCaja().getIdcaja();
                fila[3] = mov.getEstado();
                fila[4] = mov.getMonto();
                fila[5] = mov.getMontoCliente();
                fila[6] = mov.getVuelto();
                fila[7] = mov.getDetalle();
                fila[8] = mov.getHoraString();
                modeloTablaMovimientos.addRow(fila);
            }
            
        this.jTable1.setModel(modeloTablaMovimientos);
        rowSorter = new TableRowSorter<>(this.jTable1.getModel());
        jTable1.setRowSorter(rowSorter);
        
        }catch(Notificaciones ex){
            ex.printStackTrace();
        }
    }
