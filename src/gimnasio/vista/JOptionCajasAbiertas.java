    private void tablaCajasSinCierreMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaCajasSinCierreMouseClicked
        if(!tablaCajasSinCierre.getSelectionModel().isSelectionEmpty()){
            Cajadiaria cajaSeleccionada = (Cajadiaria) tablaCajasSinCierre.getValueAt(tablaCajasSinCierre.getSelectedRow(), 0);
            LocalDate fecha = Instant.ofEpochMilli(cajaSeleccionada.getApertura().getTime()).atZone(ZoneId.systemDefault()).toLocalDate();
            this.dateTimePicker1.datePicker.setDate(fecha);
            this.dateTimePicker1.timePicker.setTime(LocalTime.of(23, 59, 59));
        }
    }//GEN-LAST:event_tablaCajasSinCierreMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!tablaCajasSinCierre.getSelectionModel().isSelectionEmpty()){
            try {
                Cajadiaria cajaSeleccionada = (Cajadiaria) tablaCajasSinCierre.getValueAt(tablaCajasSinCierre.getSelectedRow(), 0);
                LocalDateTime fechaSeleccionada = dateTimePicker1.getDateTimePermissive();
                Date cierre = Date.from(fechaSeleccionada.atZone(ZoneId.systemDefault()).toInstant());
                cajaSeleccionada.setCierre(cierre);
                miControlador.altaCaja(cajaSeleccionada);
                this.modeloTablaSinCerrar.removeRow(tablaCajasSinCierre.getSelectedRow());
                Object[] fila = new Object[4];
                fila[0] = cajaSeleccionada;
                fila[1] = cajaSeleccionada.getAperturaString();
                fila[2] = cajaSeleccionada.getCierreString();
                fila[3] = cajaSeleccionada.getEstado();
                this.modeloTablaCerradas.addRow(fila);
            } catch (Notificaciones ex) {
                JOptionPane.showMessageDialog(null,ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

