    public void cargarTablaCerradas(){
        modeloTablaCerradas = new DefaultTableModel();;
        modeloTablaCerradas.addColumn("Fecha");
        modeloTablaCerradas.addColumn("Apertura");
        modeloTablaCerradas.addColumn("Cierre");
        modeloTablaCerradas.addColumn("Usuario");
        this.tablaCajasCerradas.setModel(modeloTablaCerradas);
    }
    public void cargarTablaSinCierre(){
        modeloTablaSinCerrar = new DefaultTableModel();
        modeloTablaSinCerrar.addColumn("Fecha");
        modeloTablaSinCerrar.addColumn("Hora");
        modeloTablaSinCerrar.addColumn("Usuario");
        Object[] fila = new Object[3];
        try{
            for(Cajadiaria caja : miControlador.dameCajasSinCerrar()) {
                fila[0] = caja;
                fila[1] = caja.getAperturaString();
                fila[2] = caja.getEstado();
                modeloTablaSinCerrar.addRow(fila);
            }
        }catch(Notificaciones ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }finally{
            this.tablaCajasSinCierre.setModel(modeloTablaSinCerrar);
        }
    }
