    public void cargarTablaCajas(){
        modeloTablaCajas = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        modeloTablaCajas.addColumn("IdCaja");
        modeloTablaCajas.addColumn("Usuario");
        modeloTablaCajas.addColumn("Monto Apertura");
        modeloTablaCajas.addColumn("Dia");
        modeloTablaCajas.addColumn("Hora de Cierre");
        Object[] fila = new Object[5];
        try{
            for(Cajadiaria caja:miControlador.getListaCajas()){
                fila[0] = caja.getIdcaja();
                fila[1] = caja.getEstado();
                fila[2] = caja.getMontoactual();
                fila[3] = caja;
                fila[4] = caja.getCierreString();
                modeloTablaCajas.addRow(fila);
            }
            
        this.jTable1.setModel(modeloTablaCajas);
        rowSorter = new TableRowSorter<>(this.jTable1.getModel());
        jTable1.setRowSorter(rowSorter);
        
        }catch(Notificaciones ex){
            ex.printStackTrace();
        }
    }
