    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if(!jTable1.getSelectionModel().isSelectionEmpty()){
            caja = (Cajadiaria) jTable1.getValueAt(jTable1.getSelectedRow(),3);
            this.txtIdCaja.setText(String.valueOf(caja.getIdcaja()));
            this.txtMontoApertura.setText(String.valueOf(caja.getMontoactual()));
            this.txtNombreusuario.setText(caja.getEstado());
            Instant instante = caja.getApertura().toInstant();
            LocalDateTime apertura = instante.atZone(ZoneId.systemDefault()).toLocalDateTime();
            this.timeApertura.setDateTimePermissive(apertura);
            instante = caja.getCierre().toInstant();
            apertura = instante.atZone(ZoneId.systemDefault()).toLocalDateTime();
            this.timeCierre.setDateTimePermissive(apertura);
        }
    }//GEN-LAST:event_jTable1MouseClicked

